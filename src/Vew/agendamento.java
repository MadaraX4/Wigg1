    private void jB_deletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_deletarActionPerformed
       
        try {
            (new Agendamento_DAO()).deletar(Integer.parseInt(jT_codClienete.getText()));
            JT_servico.setText("");
            JT_servico.setText("");
            jT_codClienete.setText("");
            jT_dataAgendamento.setText("");
            jT_horario.setText("");
            jT_cliente.setText("");
            JOptionPane.showMessageDialog(null, "Deletado com sucesso!");
        } catch (SQLException ex) {
            Logger.getLogger(agendamento.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jB_deletarActionPerformed

