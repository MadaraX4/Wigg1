    private void jB_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_pesquisarActionPerformed
    Agendamento a = new Agendamento();
        try {
            a = (new Agendamento_DAO()).buscar(Integer.parseInt(jT_codClienete.getText()));
            jT_cliente.setText(a.getCliente());
            JT_servico.setText(a.getServico());
            jT_dataAgendamento.setText(a.getData_agendamento());
            jT_horario.setText(a.getHorario());
        } catch (SQLException ex) {
            Logger.getLogger(agendamento.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jB_pesquisarActionPerformed

