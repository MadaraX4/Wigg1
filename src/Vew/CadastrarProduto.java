    private void jB_cadastrarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_cadastrarProdutoActionPerformed
      Produto p = new Produto();
      produto_DAO dao = new produto_DAO();
      
      p.setNome(jT_nome.getText());
      p.setDestribuidor(jT_destribuidor.getText());
      p.setData_validade(jFT_dataValidade.getText());
      p.setQuantidade(Integer.parseInt(jT_quantidade.getText()));
      p.setPreco(Double.parseDouble(jT_preco.getText()));
      
      dao.save(p);
       JOptionPane.showMessageDialog(null, "Cadastrado Com Sucesso!"); 
        
    }//GEN-LAST:event_jB_cadastrarProdutoActionPerformed

